function safeReplace(content, replacements) {
  let count = 0;
  for (const [oldText, newText] of replacements) {
    // Match only when the old text is a complete option string (surrounded by single quotes)
    const searchStr = "'" + oldText + "'";
    const replaceStr = "'" + newText + "'";
    if (content.includes(searchStr)) {
      content = content.split(searchStr).join(replaceStr);
      count++;
    }
  }
  return { content, count };
}

